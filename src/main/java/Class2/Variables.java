package Class2;

public class Variables {
    public static void main(String[] args) {

        int box1 = 10;
        System.out.println(box1);
        // To store the whole numbers without decimal, we have four types of datatype.
        // 1. byte 2. short 3. int 4. long

        byte box2 = 127;
        // The limit or range for byte is from -128 to 127
        byte box3 = -128;

        short box4 = 128;
        // The range for short is from 128 to 32768
        short box5 = 32767;

        int biggerBox = 1234567890; // Most common type of box used in Java -- its bigger box

        long maxBox = 1234567890123456789l; // type L at the end of numbers
        //********************************
        short myBox = 10000;
        System.out.println(myBox);
    }
}
