package Class2;

public class OperatorsDemo {
    public static void main(String[] args) {

        int number = 100;
        // In one class, we Cannot name two variables (boxes) with the same name, it's not allowed. Make it different
        int number2 = 200;
        System.out.println(number2+number); // To Duplicate/copy a line, Put your cursor on the line and press Ctrl+D
        System.out.println(number2-number);
        System.out.println(number2/number);
        System.out.println(number2*number);

        /*
        If you have to store a Whole Number, always use int
        If you have to store a Decimal Number, always use double
        If you have to store a String Character (multiple letters), always use String
         */


    }
}
