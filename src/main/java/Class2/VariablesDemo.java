package Class2;

public class VariablesDemo {
    public static void main(String[] args) {

        // To store a name
        String name = "Full Name: "+" Sadeea Ali"; // adding something with + sign is called: Concatenate
        // To store her age
        int age = 16;
        // To store her city
        String city = "San Jose";
        //To store her salary
        int salary = 120000;
        // To store her gender
        char gender = 'F';
        // To store her phone number, use String because we use dash/ hyphen
        String phoneNumber = "530-570-4918";
        boolean smart = true;
        //-----------------------*****************----------------------------

        System.out.println(name);
        System.out.println(age);
        System.out.println(city);
        System.out.println(salary);
        System.out.println(gender);
        System.out.println(phoneNumber);
        System.out.println(smart);

    }
}
