package Class2;

public class Operators {
    public static void main(String[] args) {

        String number1 = "10";
        String number2 = "20";
        System.out.println(number1 + number2);
        // here we cannot use operation like adding/subtracting/division with String datatype..
        // With String, it just recognize it as Text or letters

        int number3 = 30;
        int number4 = 40;
        System.out.println(number4+number3);
        System.out.println(number4*number3);
        System.out.println(number4-number3);
        System.out.println(number4/number3);

    }
}
