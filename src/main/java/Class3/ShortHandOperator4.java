package Class3;

public class ShortHandOperator4 {
    public static void main(String[] args) {

        int number = 10;
        number = number + 10;
        System.out.println(number);

        // We can use Short Hand operator
        number += 20; // Meaning --> whatever value is stored in Number variable, add 10 to it.
        System.out.println(number);

        // to subtract by using short hand operator
        int number2 = 20;
        number2 -= 10;
        System.out.println(number2);

        // to divide by using short-hand operator
        int number3 = 200;
        number3 /= 10;
        System.out.println(number3);



    }
}
