package Class6;

public class IncrementOperator {
    public static void main(String[] args) {

        int number = 10; // Now number is 10
        number = number+1; // here it becomes 11 because we incremented it by 1, then assigned it to number
        System.out.println(number);
        number += 1; // here we incremented by 1, it becomes 12

        System.out.println(number);
        number ++; // it also increments the number by 1 , becomes 13
        System.out.println(number);

        System.out.println("-----------------Decrement number ----------------------\n");

        int number2 = 10; // now number is 10
        number2 --; // decremented value by 1, becomes 9
        System.out.println(number2);






    }
}
