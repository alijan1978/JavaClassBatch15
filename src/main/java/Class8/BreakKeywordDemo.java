package Class8;

public class BreakKeywordDemo{
    public static void main(String[] args) {

        for (int i = 0; i <10 ; i++) {

            if(i==3){ // We break our iteration process when it reaches before 6... Break should be with if condition
                break;
            }
            System.out.println(i);
        }
        System.out.println("Last line in our code");
    }
}




















































































































































































