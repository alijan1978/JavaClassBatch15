package Class8;

import java.util.Scanner;

public class TaskFromRepil63 {
    public static void main(String[] args) {

        Scanner fetch = new Scanner(System.in);
        System.out.println("Please enter the number");

        int number = fetch.nextInt(); // allows the code to print the previous numbers
        for (int i = 0; i < number; i++) {
            System.out.print(i+" "); // prints the number on the same line, by using " " add space between
        }
    }
}






















































































